// Función para obtener noticias relacionadas
export function obtenerNoticiasRelacionadas(noticiaActual: any, cantidad: number = 3) {
  // Filtrar noticias de la misma categoría (excluyendo la actual)
  const mismaCategoria = noticias
    .filter(n => n.categoria === noticiaActual.categoria && n.id !== noticiaActual.id)
    .sort((a, b) => new Date(b.fecha).getTime() - new Date(a.fecha).getTime());

  // Si no hay suficientes de la misma categoría, agregar las más recientes de cualquier categoría
  const otrasCategorias = noticias
    .filter(n => n.categoria !== noticiaActual.categoria && n.id !== noticiaActual.id)
    .sort((a, b) => new Date(b.fecha).getTime() - new Date(a.fecha).getTime());

  // Combinar y limitar a la cantidad solicitada
  const relacionadas = [...mismaCategoria, ...otrasCategorias].slice(0, cantidad);

  return relacionadas;
} 